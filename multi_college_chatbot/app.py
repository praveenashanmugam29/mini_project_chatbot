def verify_password(stored_password_hash_or_plain: str, provided_password: str) -> bool:
    """
    Supports BOTH:
    1) Hashed passwords (recommended): generated by werkzeug.security.generate_password_hash
    2) Plain text passwords (not recommended, but beginner-friendly for demo DB inserts)

    If the stored value looks like a werkzeug hash, we verify it as a hash.
    Otherwise we compare plain text.
    """
    if not stored_password_hash_or_plain:
        return False
    s = str(stored_password_hash_or_plain)
    if s.startswith("pbkdf2:") or s.startswith("scrypt:"):
        return check_password_hash(s, provided_password)
    return s == provided_password


