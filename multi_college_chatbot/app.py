def generate_captcha() -> Tuple[str, int]:
    """
    Generates a simple math captcha like '7 + 5 = ?'
    Returns: (question_text, answer_int)
    """
    import random

    a = random.randint(2, 9)
    b = random.randint(2, 9)
    op = random.choice(["+", "-"])
    if op == "-":
        if b > a:
            a, b = b, a
        answer = a - b
    else:
        answer = a + b
    return f"{a} {op} {b} = ?", answer


