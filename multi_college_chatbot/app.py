def ensure_session_id():
    """
    We store a unique session id so we can save chat history even for visitors.
    """
    if "sid" not in session:
        session["sid"] = str(uuid.uuid4())


def login_required(view_func):
    """
    Simple decorator to protect pages.
    """

    def wrapper(*args, **kwargs):
        ensure_session_id()
        if not session.get("logged_in"):
            return redirect(url_for("login"))
        return view_func(*args, **kwargs)

    # Keep function name for Flask routing
    wrapper.__name__ = view_func.__name__
    return wrapper


def college_required(view_func):
    """
    Decorator: user must select a college before entering dashboard/chat pages.
    """

    def wrapper(*args, **kwargs):
        ensure_session_id()
        if not session.get("logged_in"):
            return redirect(url_for("login"))
        if not session.get("college_id"):
            return redirect(url_for("select_college"))
        return view_func(*args, **kwargs)

    wrapper.__name__ = view_func.__name__
    return wrapper


