@app.route("/api/chat", methods=["POST"])
@college_required
def api_chat():
    ensure_session_id()
    college_id = int(session["college_id"])
    data = request.get_json(silent=True) or {}
    message = (data.get("message") or "").strip()

    if not message:
        return jsonify({"ok": False, "error": "Empty message"}), 400

    payload = chatbot_reply(college_id, message)
    save_chat_history(college_id, message, payload.get("reply", ""))
    return jsonify({"ok": True, **payload})


@app.route("/api/enquiry", methods=["POST"])
@college_required
def api_enquiry():
    """
    Save enquiry form data.
    """
    ensure_session_id()
    college_id = int(session["college_id"])
    data = request.get_json(silent=True) or {}

    name = (data.get("name") or session.get("display_name") or "").strip()
    email = (data.get("email") or "").strip()
    phone = (data.get("phone") or "").strip()
    message = (data.get("message") or "").strip()

    if not name or not message:
        return jsonify({"ok": False, "error": "Name and message are required"}), 400

    execute(
        """
        INSERT INTO enquiries (user_id, user_type, college_id, name, email, phone, message, created_at)
        VALUES (%s, %s, %s, %s, %s, %s, %s, NOW())
        """,
        (
            session.get("user_id"),
            session.get("user_type"),
            college_id,
            name,
            email,
            phone,
            message,
        ),
    )
    return jsonify({"ok": True})


