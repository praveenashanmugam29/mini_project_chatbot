@app.route("/login", methods=["GET", "POST"])
def login():
    ensure_session_id()

    # Create captcha for visitor login
    if request.method == "GET":
        question, answer = generate_captcha()
        session["captcha_answer"] = answer
        return render_template(
            "login.html",
            app_name=app.config["APP_NAME"],
            institution=app.config["INSTITUTION_NAME"],
            captcha_question=question,
        )

    user_type = request.form.get("user_type", "")

    try:
        # A) Existing Student (college mail + password in student_login table)
        if user_type == "existing_student":
            email = request.form.get("student_email", "").strip().lower()
            password = request.form.get("student_password", "")

            row = fetch_one(
                "SELECT * FROM student_login WHERE college_email=%s",
                (email,),
            )
            if not row or not verify_password(row["password_hash"], password):
                flash("Invalid student email or password.", "danger")
                return redirect(url_for("login"))

            session["logged_in"] = True
            session["user_type"] = "existing_student"
            session["user_id"] = row["id"]
            session["display_name"] = row.get("student_name") or "Student"

            # For existing student, we can auto set college if available
            if row.get("college_id"):
                session["college_id"] = int(row["college_id"])
                return redirect(url_for("dashboard"))
            return redirect(url_for("select_college"))

        # B) New Student for Admission (register/login using Gmail)
        if user_type == "new_student":
            email = request.form.get("new_email", "").strip().lower()
            password = request.form.get("new_password", "")

            user = fetch_one(
                "SELECT * FROM users WHERE user_type='new_student' AND email=%s",
                (email,),
            )
            if not user:
                flash("Account not found. Please register first.", "warning")
                return redirect(url_for("login"))

            if not verify_password(user["password_hash"], password):
                flash("Invalid email or password.", "danger")
                return redirect(url_for("login"))

            session["logged_in"] = True
            session["user_type"] = "new_student"
            session["user_id"] = user["id"]
            session["display_name"] = user.get("name") or "New Student"
            return redirect(url_for("select_college"))

        # C) Visitor / Parent (name + captcha)
        if user_type == "visitor":
            name = request.form.get("visitor_name", "").strip()
            captcha = request.form.get("captcha", "").strip()

            try:
                captcha_int = int(captcha)
            except ValueError:
                captcha_int = None

            if not name:
                flash("Please enter your name.", "warning")
                return redirect(url_for("login"))

            if captcha_int is None or captcha_int != session.get("captcha_answer"):
                flash("Captcha verification failed. Please try again.", "danger")
                return redirect(url_for("login"))

            # Store visitor as a lightweight user (optional)
            session["logged_in"] = True
            session["user_type"] = "visitor"
            session["user_id"] = None
            session["display_name"] = name
            return redirect(url_for("select_college"))

        flash("Please choose a valid login type.", "warning")
        return redirect(url_for("login"))

    except MySQLError as e:
        flash(f"MySQL error: {e}", "danger")
        return redirect(url_for("login"))


@app.route("/register", methods=["POST"])
def register():
    """
    Register new student (Gmail id + password).
    """
    ensure_session_id()
    name = request.form.get("reg_name", "").strip()
    email = request.form.get("reg_email", "").strip().lower()
    password = request.form.get("reg_password", "")

    if not name or not email or not password:
        flash("Please fill all registration fields.", "warning")
        return redirect(url_for("login"))

    # Small validation (only allow Gmail for demo)
    if not email.endswith("@gmail.com"):
        flash("For this demo, please use a Gmail address (example@gmail.com).", "info")
        return redirect(url_for("login"))

    existing = fetch_one(
        "SELECT id FROM users WHERE user_type='new_student' AND email=%s",
        (email,),
    )
    if existing:
        flash("Account already exists. Please login.", "info")
        return redirect(url_for("login"))

    execute(
        """
        INSERT INTO users (user_type, name, email, password_hash, created_at)
        VALUES ('new_student', %s, %s, %s, NOW())
        """,
        (name, email, generate_password_hash(password)),
    )
    flash("Registration successful! Please login now.", "success")
    return redirect(url_for("login"))


