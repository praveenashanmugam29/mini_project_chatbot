CHATBOT_CACHE: Dict[int, Dict[str, Any]] = {}


def get_college(college_id: int) -> Optional[Dict[str, Any]]:
    return fetch_one("SELECT * FROM colleges WHERE id=%s", (college_id,))


def get_college_details(college_id: int) -> Optional[Dict[str, Any]]:
    return fetch_one("SELECT * FROM college_details WHERE college_id=%s", (college_id,))


def build_chatbot_model(college_id: int) -> Dict[str, Any]:
    """
    Load chatbot questions for a college and build a TF-IDF model.
    """
    rows = fetch_all(
        """
        SELECT id, category, question, answer
        FROM chatbot_questions
        WHERE college_id=%s
        ORDER BY id ASC
        """,
        (college_id,),
    )

    model: Dict[str, Any] = {"rows": rows, "built_at": datetime.utcnow()}

    # If sklearn is installed, use TF-IDF similarity (better matching).
    if SKLEARN_AVAILABLE and rows:
        corpus = [r["question"] for r in rows]
        vectorizer = TfidfVectorizer(stop_words="english")
        X = vectorizer.fit_transform(corpus)
        model.update({"vectorizer": vectorizer, "X": X})

    CHATBOT_CACHE[college_id] = model
    return model


def get_chatbot_model(college_id: int) -> Dict[str, Any]:
    """
    Get cached model if available; otherwise build it.
    """
    if college_id not in CHATBOT_CACHE:
        return build_chatbot_model(college_id)
    return CHATBOT_CACHE[college_id]


def is_hostel_image_request(text: str) -> bool:
    t = text.lower()
    triggers = [
        "hostel image",
        "hostel images",
        "show hostel",
        "show hostel room",
        "hostel room",
        "boys hostel",
        "girls hostel",
    ]
    return any(k in t for k in triggers)


def is_facility_image_request(text: str) -> bool:
    """
    Detects if user is asking for images for facilities like labs, classrooms, library, etc.
    """
    t = text.lower()

    # If user explicitly says image/photo/show/view + facility keyword
    wants_image = any(k in t for k in ["image", "images", "photo", "photos", "picture", "show", "view"])
    has_facility_keyword = any(
        k in t
        for k in [
            "lab",
            "labs",
            "laboratory",
            "classroom",
            "class room",
            "smart class",
            "library",
            "infrastructure",
            "campus",
            "placement",
            "placements",
            "facility",
            "facilities",
        ]
    )

    # Also support direct triggers like "show labs" or "library image"
    direct_triggers = [
        "lab image",
        "labs image",
        "show labs",
        "classroom image",
        "library image",
        "infrastructure image",
        "placement image",
        "campus image",
        "campus facilities",
    ]

    return (wants_image and has_facility_keyword) or any(k in t for k in direct_triggers)


def guess_facility_type(text: str) -> str:
    """
    Returns one of: labs, classrooms, library, infrastructure, placements, campus
    """
    t = text.lower()
    if "lab" in t or "labs" in t or "laboratory" in t:
        return "labs"
    if "classroom" in t or "class room" in t or "smart class" in t:
        return "classrooms"
    if "library" in t:
        return "library"
    if "placement" in t or "placements" in t:
        return "placements"
    if "infrastructure" in t:
        return "infrastructure"
    return "campus"


def guess_hostel_gender(text: str) -> str:
    t = text.lower()
    if "boys" in t:
        return "boys"
    if "girls" in t:
        return "girls"
    return "common"


def clean_text(s: str) -> str:
    s = s.strip().lower()
    s = re.sub(r"\s+", " ", s)
    return s


def get_suggestions(college_id: int) -> List[str]:
    """
    Quick FAQ suggestions shown as chips in UI.
    """
    rows = fetch_all(
        """
        SELECT question
        FROM chatbot_questions
        WHERE college_id=%s
        ORDER BY id ASC
        LIMIT 6
        """,
        (college_id,),
    )
    return [r["question"] for r in rows] if rows else []


def chatbot_reply(college_id: int, user_text: str) -> Dict[str, Any]:
    """
    Returns a dict:
    - type: "text" | "images"
    - reply: string
    - images: [ {url, title} ] (only for type="images")
    - suggestions: list of suggestion strings
    """
    user_text_clean = clean_text(user_text)

    # Special Feature: Hostel images
    if is_hostel_image_request(user_text_clean):
        gender = guess_hostel_gender(user_text_clean)
        imgs = fetch_all(
            """
            SELECT image_filename, title, gender
            FROM hostel_images
            WHERE college_id=%s AND (gender=%s OR gender='common')
            ORDER BY id ASC
            """,
            (college_id, gender),
        )
        if not imgs:
            return {
                "type": "text",
                "reply": "I don’t have hostel images stored yet. Please add images in static/images/hostel and insert rows into hostel_images table.",
                "suggestions": get_suggestions(college_id),
            }

        images_payload = [
            {
                "url": url_for("static", filename=f"images/hostel/{i['image_filename']}"),
                "title": i.get("title") or "Hostel Room",
            }
            for i in imgs
        ]
        return {
            "type": "images",
            "reply": "Here are some hostel room images:",
            "images": images_payload,
            "suggestions": get_suggestions(college_id),
        }

    # Special Feature: Facility images (labs, classrooms, library, infrastructure, placements, campus)
    if is_facility_image_request(user_text_clean):
        facility = guess_facility_type(user_text_clean)
        imgs = fetch_all(
            """
            SELECT image_filename, title, facility_type
            FROM facility_images
            WHERE college_id=%s AND facility_type=%s
            ORDER BY id ASC
            """,
            (college_id, facility),
        )
        if not imgs:
            return {
                "type": "text",
                "reply": (
                    f"I don’t have {facility} images stored yet. "
                    "Please add images inside static/images/facilities and insert rows into facility_images table."
                ),
                "suggestions": get_suggestions(college_id),
            }

        images_payload = [
            {
                "url": url_for("static", filename=f"images/facilities/{i['image_filename']}"),
                "title": i.get("title") or facility.title(),
            }
            for i in imgs
        ]
        return {
            "type": "images",
            "reply": f"Here are some {facility} images:",
            "images": images_payload,
            "suggestions": get_suggestions(college_id),
        }

    # Normal FAQ matching
    model = get_chatbot_model(college_id)
    rows = model["rows"]

    if rows:
        # If sklearn is available, use TF-IDF cosine similarity
        if SKLEARN_AVAILABLE and model.get("vectorizer") is not None and model.get("X") is not None:
            vectorizer = model["vectorizer"]
            X = model["X"]
            user_vec = vectorizer.transform([user_text_clean])
            sims = cosine_similarity(user_vec, X).flatten()
            best_idx = int(sims.argmax())
            best_score = float(sims[best_idx])

            # Threshold decides when the bot is confident
            if best_score >= 0.22:
                answer = rows[best_idx]["answer"]
                return {"type": "text", "reply": answer, "suggestions": get_suggestions(college_id)}

        # Fallback: difflib similarity (works even without scikit-learn)
        import difflib

        qlist = [str(r["question"]).lower() for r in rows]
        scores = [difflib.SequenceMatcher(None, user_text_clean, q).ratio() for q in qlist]
        best_idx = int(max(range(len(scores)), key=lambda i: scores[i]))
        best_score = float(scores[best_idx])
        if best_score >= 0.42:
            answer = rows[best_idx]["answer"]
            return {"type": "text", "reply": answer, "suggestions": get_suggestions(college_id)}

    # Fallback answer (when no match)
    details = get_college_details(college_id) or {}
    contact = details.get("contact_text") or "Please visit the college office or contact the admissions team."
    return {
        "type": "text",
        "reply": (
            "I’m not fully sure about that yet. You can ask about fees, courses, hostel, placements, "
            "admission process, scholarships, transport, labs, infrastructure, library, events, etc.\n\n"
            f"Contact: {contact}"
        ),
        "suggestions": get_suggestions(college_id),
    }


def save_chat_history(college_id: int, user_message: str, bot_reply: str):
    """
    Save chat history to database.
    """
    ensure_session_id()
    execute(
        """
        INSERT INTO chat_history (session_id, user_id, user_type, college_id, user_message, bot_reply, created_at)
        VALUES (%s, %s, %s, %s, %s, %s, NOW())
        """,
        (
            session.get("sid"),
            session.get("user_id"),
            session.get("user_type"),
            college_id,
            user_message,
            bot_reply,
        ),
    )


